x = 24
y = 20
list = [10, 20, 30, 40, 50]

print(f"x = {x}, y = {20}")
print(f"List: {list}")

if (x not in list):
    print(f"{x} is NOT present in given list {list}")
else:
    print(f"{x} is present in given list {list}")

if (y in list):
    print(f"{y} is present in given list {list}")
else:
    print(f"{y} is NOT present in given list {list}")
